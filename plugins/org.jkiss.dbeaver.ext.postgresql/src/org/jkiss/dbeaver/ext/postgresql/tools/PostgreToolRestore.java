package org.jkiss.dbeaver.ext.postgresql.tools;

import org.eclipse.ui.IWorkbenchPart;
import org.eclipse.ui.IWorkbenchWindow;
import org.jkiss.dbeaver.DBException;
import org.jkiss.dbeaver.ext.postgresql.model.PostgreDatabase;
import org.jkiss.dbeaver.ext.postgresql.model.PostgreSchema;
import org.jkiss.dbeaver.model.struct.DBSObject;
import org.jkiss.dbeaver.tools.IExternalTool;
import org.jkiss.dbeaver.ui.dialogs.tools.ToolWizardDialog;

import java.util.Collection;

/**
 * Database import
 */
public class PostgreToolRestore implements IExternalTool
{
    @Override
    public void execute(IWorkbenchWindow window, IWorkbenchPart activePart, Collection<DBSObject> objects) throws DBException
    {
        for (DBSObject object : objects) {
            PostgreDatabase database;
            if (object instanceof PostgreSchema) {
                database = ((PostgreSchema) object).getDatabase();
            } else if (object instanceof PostgreDatabase) {
                database = (PostgreDatabase) object;
            } else {
                continue;
            }
            ToolWizardDialog dialog = new ToolWizardDialog(
                window,
                new PostgreRestoreWizard(database));
            dialog.open();
        }
    }
}
