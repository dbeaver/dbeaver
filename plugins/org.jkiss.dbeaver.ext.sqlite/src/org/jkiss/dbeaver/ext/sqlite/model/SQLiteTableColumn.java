package org.jkiss.dbeaver.ext.sqlite.model;

import org.jkiss.dbeaver.ext.generic.model.GenericTable;
import org.jkiss.dbeaver.ext.generic.model.GenericTableColumn;

public class SQLiteTableColumn extends GenericTableColumn {

    public SQLiteTableColumn(GenericTable table, String columnName, String typeName, int valueType, int sourceType, int ordinalPosition, long columnSize, long charLength, Integer scale, Integer precision, int radix, boolean notNull, String remarks, String defaultValue, boolean autoIncrement, boolean autoGenerated) {
        super(table, columnName, typeName, valueType, sourceType, ordinalPosition, columnSize, charLength, scale, precision, radix, notNull, remarks, defaultValue, autoIncrement, autoGenerated);
    }

    // Not a property
    @Override
    public boolean isAutoGenerated() {
        return super.isAutoGenerated();
    }

    // Not a property
    @Override
    public long getMaxLength() {
        return super.getMaxLength();
    }

    // Not a property
    @Override
    public Integer getScale() {
        return super.getScale();
    }

    // Not a property
    @Override
    public Integer getPrecision() {
        return super.getPrecision();
    }
}
